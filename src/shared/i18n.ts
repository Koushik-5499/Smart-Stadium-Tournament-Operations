/**
 * Returns the translated string for a given key and language.
 * Falls back to English if the key is not found in the target language.
 *
 * @param key - The translation key (e.g., 'nav.home')
 * @param language - The target language code
 * @returns The translated string
 */
export function t(key: string, language: SupportedLanguage = 'en'): string {
  return translations[language]?.[key] ?? translations.en[key] ?? key;
}

/**
 * Returns the text direction for a given language.
 *
 * @param language - The language code
 * @returns 'rtl' for Arabic, 'ltr' for all others
 */
export function getTextDirection(language: SupportedLanguage): 'ltr' | 'rtl' {
  return language === 'ar' ? 'rtl' : 'ltr';
}
